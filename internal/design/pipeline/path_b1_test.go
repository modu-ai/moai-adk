// parseFrontmatter parses YAML frontmatter (--- ... ---) into key-value pairs.
// Only simple key: value form is supported; YAML arrays are handled separately.
func parseFrontmatter(t *testing.T, content string) map[string]string {
	t.Helper()

	result := make(map[string]string)
	inFrontmatter := false
	scanner := bufio.NewScanner(strings.NewReader(content))

	lineCount := 0
	for scanner.Scan() {
		line := scanner.Text()
		if line == "---" {
			lineCount++
			if lineCount == 1 {
				inFrontmatter = true
				continue
			}
			if lineCount == 2 {
				break
			}
		}
		if !inFrontmatter {
			continue
		}

		// Simple key: value parsing (strip surrounding quotes).
		idx := strings.Index(line, ":")
		if idx < 0 {
			continue
		}
		key := strings.TrimSpace(line[:idx])
		val := strings.TrimSpace(line[idx+1:])
		val = strings.Trim(val, `"'`)
		result[key] = val
	}

	return result
}

// TestPathB1_FigmaExtractorSkillFrontmatter verifies the stub SKILL.md frontmatter.
// REQ-DPL-002: confirms inclusion of Figma file ID, page selectors, credential reference.
func TestPathB1_FigmaExtractorSkillFrontmatter(t *testing.T) {
	skillsDir := t.TempDir()
	skillPath := writeStubFigmaExtractorSkill(t, skillsDir)

	data, err := os.ReadFile(skillPath)
	if err != nil {
		t.Fatalf("SKILL.md мқҪкё° мӢӨнҢЁ: %v", err)
	}

	content := string(data)
	fm := parseFrontmatter(t, content)

	// figma_file_id must exist and be non-empty.
	if fm["figma_file_id"] == "" {
		t.Error("frontmatterм—җ figma_file_id м—ҶмқҢ")
	}

	// figma_credential_ref must exist.
	if fm["figma_credential_ref"] == "" {
		t.Error("frontmatterм—җ figma_credential_ref м—ҶмқҢ")
	}

	// figma_page_selectors must exist (verified via content substring).
	if !strings.Contains(content, "figma_page_selectors") {
		t.Error("SKILL.mdм—җ figma_page_selectors м—ҶмқҢ")
	}

	t.Logf("figma_file_id: %s", fm["figma_file_id"])
	t.Logf("figma_credential_ref: %s", fm["figma_credential_ref"])
}

// TestPathB1_GeneratedTokensValidate: tokens.json generated by Path B1 must pass DTCG 2025.10 validation.
func TestPathB1_GeneratedTokensValidate(t *testing.T) {
	// Simulate tokens.json produced by the meta-harness figma-extractor.
	simulatedFigmaTokens := map[string]any{
		"color-primary": map[string]any{
			"$type":  "color",
			"$value": "#0F172A",
		},
		"color-accent": map[string]any{
			"$type":  "color",
			"$value": "#6366F1",
		},
		"font-heading": map[string]any{
			"$type":  "fontFamily",
			"$value": []any{"Geist", "sans-serif"},
		},
		"space-base": map[string]any{
			"$type":  "dimension",
			"$value": "4px",
		},
	}

	report, err := dtcg.Validate(simulatedFigmaTokens)
	if err != nil {
		t.Fatalf("Validate() мӢӨн–ү мӢӨнҢЁ: %v", err)
	}

	if !report.Valid {
		t.Errorf("Path B1 нҶ нҒ°мқҙ DTCG кІҖмҰқ мӢӨнҢЁ. мҳӨлҘҳ %dк°ң:", len(report.Errors))
		for _, e := range report.Errors {
			t.Logf("  - %s", e.Error())
		}
	}

	t.Logf("Path B1 нҶ нҒ° %dк°ң кІҖмҰқ нҶөкіј", report.TokenCount)
}

// TestPathB1_PathSelectionRecordsB1: "B1" recorded in path-selection.json.
